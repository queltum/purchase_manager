class StatsData:
	def __init__(
		self,
		total_items: str,
		total_amount: str,
		average_amount: str,
		bought_amount: str,
		planned_amount: str
	):
		self.total_items = total_items
		self.total_amount = total_amount
		self.average_amount = average_amount
		self.bought_amount = bought_amount
		self.planned_amount = planned_amount